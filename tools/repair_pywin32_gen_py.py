from __future__ import annotations

import glob
import os
import shutil
import sys
import types


def main() -> int:
    try:
        import win32com.client.gencache as gencache  # type: ignore
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] failed to import win32com: {exc}")
        return 1

    try:
        gen_path = gencache.GetGeneratePath()
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] failed to get gen_py path: {exc}")
        return 1

    print(f"[repair] gen_py path: {gen_path}")

    # Ensure `win32com.gen_py` is importable as a package pointing at gen_path.
    try:
        os.makedirs(gen_path, exist_ok=True)
        init_path = os.path.join(gen_path, "__init__.py")
        if not os.path.exists(init_path):
            with open(init_path, "w", encoding="utf-8") as handle:
                handle.write("# generated by pywin32\n")
        if "win32com.gen_py" not in sys.modules:
            gen_py = types.ModuleType("win32com.gen_py")
            gen_py.__path__ = [gen_path]  # type: ignore[attr-defined]
            sys.modules[gen_py.__name__] = gen_py
        else:
            module = sys.modules.get("win32com.gen_py")
            try:
                module.__path__ = [gen_path]  # type: ignore[attr-defined]
            except Exception:
                pass
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] warning: failed to ensure win32com.gen_py package: {exc}")

    # Remove Word typelib folders first (common corruption source).
    try:
        pattern = os.path.join(gen_path, "00020905-0000-0000-C000-000000000046*")
        for candidate in glob.glob(pattern):
            print(f"[repair] removing: {candidate}")
            shutil.rmtree(candidate, ignore_errors=True)
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] warning: failed to remove Word typelib folders: {exc}")

    # Remove the whole cache to force regeneration.
    try:
        print("[repair] clearing gen_py cache contents...")
        for name in os.listdir(gen_path):
            if name in {"__init__.py", "__pycache__"}:
                continue
            target = os.path.join(gen_path, name)
            try:
                if os.path.isdir(target):
                    shutil.rmtree(target, ignore_errors=True)
                else:
                    os.remove(target)
            except Exception:
                pass
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] warning: failed to clear gen_py cache: {exc}")

    try:
        gencache.is_readonly = False
    except Exception:
        pass

    try:
        print("[repair] rebuilding cache...")
        gencache.Rebuild()
        print("[repair] rebuild done.")
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] rebuild failed: {exc}")

    # Optional: try to generate Word typelib stubs (ignore failures).
    try:
        from win32com.client import makepy  # type: ignore

        makepy.GenerateFromTypeLibSpec("Microsoft Word 16.0 Object Library")
        print("[repair] makepy Word typelib generated.")
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] makepy skipped/failed: {exc}")

    # Force wrapper generation if possible.
    try:
        import win32com.client as win32  # type: ignore

        app = gencache.EnsureDispatch("Word.Application")
        try:
            app.Visible = False
            app.DisplayAlerts = 0
        except Exception:
            pass
        try:
            app.Quit()
        except Exception:
            pass
        print(f"[repair] constants available? {hasattr(win32.constants, 'wdPageBreak')}")
    except Exception as exc:  # noqa: BLE001
        print(f"[repair] EnsureDispatch skipped/failed: {exc}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
